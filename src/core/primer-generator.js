'use strict';

const fs   = require('fs');
const path = require('path');

const SECTION_MARKERS = {
  stack:    '<!-- agents-skills:stack -->',
  skills:   '<!-- agents-skills:skills -->',
  commands: '<!-- agents-skills:commands -->',
};

/**
 * Generate (or incrementally update) all primer files:
 *   .claude/CLAUDE.md
 *   .cursor/.cursorrules
 *   .github/copilot-instructions.md
 *
 * @param {string}   workspaceDir
 * @param {object}   profile         - output of detectProject()
 * @param {string[]} installedSkills - skill names from .agents/skills/
 */
function generatePrimer(workspaceDir, profile, installedSkills) {
  const commands = _readCommands(workspaceDir);

  _writeFile(
    path.join(workspaceDir, '.claude', 'CLAUDE.md'),
    _buildClaudeContent(profile, installedSkills, commands),
    profile, installedSkills, commands,
  );

  _writeFile(
    path.join(workspaceDir, '.cursor', '.cursorrules'),
    _buildCursorContent(profile, installedSkills, commands),
    profile, installedSkills, commands,
  );

  _writeFile(
    path.join(workspaceDir, '.github', 'copilot-instructions.md'),
    _buildCopilotContent(profile, installedSkills, commands),
    profile, installedSkills, commands,
  );
}

// ── file writer (create or incremental update) ────────────────────────────────

function _writeFile(outPath, freshContent, profile, installedSkills, commands) {
  fs.mkdirSync(path.dirname(outPath), { recursive: true });

  if (!fs.existsSync(outPath)) {
    fs.writeFileSync(outPath, freshContent, 'utf8');
    return;
  }

  let existing = fs.readFileSync(outPath, 'utf8');
  existing = _replaceSection(existing, 'stack',    _stackBlock(profile));
  existing = _replaceSection(existing, 'skills',   _skillsBlock(installedSkills));
  existing = _replaceSection(existing, 'commands', _commandsBlock(commands));
  fs.writeFileSync(outPath, existing, 'utf8');
}

// ── content builders per IDE ──────────────────────────────────────────────────

function _buildClaudeContent(profile, skills, commands) {
  return _buildSections(
    '# Project Primer',
    '> Auto-generated by `agents-skills init --primer`. Edit sections outside the managed blocks freely.',
    profile, skills, commands,
  );
}

function _buildCursorContent(profile, skills, commands) {
  return _buildSections(
    '# Cursor Rules — Project Primer',
    '> Auto-generated by `agents-skills init --primer`. Cursor reads this file to understand your project context.',
    profile, skills, commands,
  );
}

function _buildCopilotContent(profile, skills, commands) {
  return _buildSections(
    '# GitHub Copilot Instructions',
    '> Auto-generated by `agents-skills init --primer`. Copilot reads this file for project-specific guidance.',
    profile, skills, commands,
  );
}

function _buildSections(heading, preamble, profile, skills, commands) {
  return [
    heading,
    '',
    preamble,
    '',
    '## Tech Stack',
    SECTION_MARKERS.stack,
    _stackBlock(profile),
    SECTION_MARKERS.stack,
    '',
    '## Active Skills',
    SECTION_MARKERS.skills,
    _skillsBlock(skills),
    SECTION_MARKERS.skills,
    '',
    '## Available Commands',
    SECTION_MARKERS.commands,
    _commandsBlock(commands),
    SECTION_MARKERS.commands,
    '',
  ].join('\n');
}

// ── shared block renderers ────────────────────────────────────────────────────

function _stackBlock(profile) {
  return [
    `- **Framework**: ${profile.framework}`,
    `- **Language**: ${profile.language}`,
  ].join('\n');
}

function _skillsBlock(skills) {
  if (skills.length === 0) return '_(no skills installed)_';
  return skills.map(s => `- ${s}`).join('\n');
}

function _commandsBlock(commands) {
  if (commands.length === 0) return '_(no commands found)_';
  return commands.map(c => `- \`/${c}\``).join('\n');
}

function _readCommands(workspaceDir) {
  const cmdsDir = path.join(workspaceDir, '.agents', 'commands');
  if (!fs.existsSync(cmdsDir)) return [];
  return fs.readdirSync(cmdsDir)
    .filter(f => f.endsWith('.md'))
    .map(f => f.replace(/\.md$/, ''))
    .sort();
}

function _replaceSection(content, key, newBlock) {
  const marker  = SECTION_MARKERS[key];
  const escaped = marker.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
  const re      = new RegExp(`${escaped}[\\s\\S]*?${escaped}`, 'g');
  return content.replace(re, `${marker}\n${newBlock}\n${marker}`);
}

module.exports = { generatePrimer };
